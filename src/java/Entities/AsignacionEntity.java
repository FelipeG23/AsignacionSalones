package Entities;

/**
 *
 * @author JUAN
 */
public class AsignacionEntity {
    
    private String codigoMateria;
    private String nombreMateria;
    private String fechaAsignada;
    private String horaInicio;
    private String horaFin;
    private String diaSemana;
    private String salonCodigo;
    
    
    
    public String getCodigoMateria() {
        return codigoMateria;
    }

    public void setCodigoMateria(String codigoMateria) {
        this.codigoMateria = codigoMateria;
    }

    public String getNombreMateria() {
        return nombreMateria;
    }

    public void setNombreMateria(String nombreMateria) {
        this.nombreMateria = nombreMateria;
    }

    public String getFechaAsignada() {
        return fechaAsignada;
    }

    public void setFechaAsignada(String fechaAsignada) {
        this.fechaAsignada = fechaAsignada;
    }

    public String getHoraInicio() {
        return horaInicio;
    }

    public void setHoraInicio(String horaInicio) {
        this.horaInicio = horaInicio;
    }

    public String getHoraFin() {
        return horaFin;
    }

    public void setHoraFin(String horaFin) {
        this.horaFin = horaFin;
    }

    public String getDiaSemana() {
        return diaSemana;
    }

    public void setDiaSemana(String diaSemana) {
        this.diaSemana = diaSemana;
    }

    public String getSalonCodigo() {
        return salonCodigo;
    }

    public void setSalonCodigo(String salonCodigo) {
        this.salonCodigo = salonCodigo;
    }
    
    
    
    
}
